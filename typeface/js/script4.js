$('.letter_t').on('mouseover', function () {
     console.log(this);
  $(".letter_t p").show();
  $('.img_wave').show();
  if (soundon == false) {
        document.getElementById("waveaudio1").play();
        soundon = true; 
    } else {
        document.getElementById("waveaudio1").pause();
        soundon = false;
    }
  
});

